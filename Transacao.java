import java.util.Date;

public class Transacao {
    private double valor;
    private String tipo;  
    private String descricao;
    private Date data;

    public Transacao(double valor, String tipo, String descricao, Date data) {
        this.valor = valor;
        this.tipo = tipo;
        this.descricao = descricao;
        this.data = data;
    }

    public double getValor() {
        return valor;
    }

    public String getTipo() {
        return tipo;
    }

    public String getDescricao() {
        return descricao;
    }

    public Date getData() {
        return data;
    }

    @Override
    public String toString() {
        return "Data: " + data + " | Tipo: " + tipo + " | Descrição: " + descricao + " | Valor: R$ " + valor;
    }
}
